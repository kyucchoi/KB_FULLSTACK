package variable;

public class Var1 {
    public static void main(String[] args) {
        int a = 10;
        double b = 4.3;
        boolean c = true;
        char d = 'c';
        String e = "Hello Java World!";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
